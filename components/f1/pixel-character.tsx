function decodeGrid(rows: string[]): (string | null)[][] {
  return rows.map((row) =>
    row.split("").map((ch) => {
      const color = PALETTE[ch];
      return !color || color === "transparent" ? null : color;
    })
  );
}

interface PixelCharacterProps {
  characterId: string;
  className?: string;
}

export function PixelCharacter({ characterId, className }: PixelCharacterProps) {
  const raw = CHARACTERS[characterId];
  if (!raw) return null;

  const grid = decodeGrid(raw);

  return (
    <svg
      viewBox="0 0 96 192"
      className={className}
      style={{ imageRendering: "pixelated" }}
    >
      {grid.map((row, y) =>
        row.map((color, x) =>
          color ? (
            <motion.rect
              key={`${y}-${x}`}
              x={x * 6}
              y={y * 6}
              width={6}
              height={6}
              fill={color}
              initial={{ opacity: 0 }}
              animate={{ opacity: 1 }}
              transition={{ delay: y * 0.02, duration: 0.05 }}
            />
          ) : null
        )
      )}
    </svg>
  );
}
